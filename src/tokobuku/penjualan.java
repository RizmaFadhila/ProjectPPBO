    private void txtcariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcariKeyReleased
        // TODO add your handling code here:
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        ListBuku.clear();
        ListBuku=ob.cariBuku(txtcari.getText());
        for(int x=0;x<ListBuku.size();x++){
            Object[] data = new Object[6];
            data[0]= ListBuku.get(x).getId();
            data[1]= ListBuku.get(x).getJDL();
            data[2]= ListBuku.get(x).getGenre();
            data[3]= ListBuku.get(x).getPenulis();
            data[4]= ListBuku.get(x).getHarga();
            data[5]= ListBuku.get(x).getStok();
            model.addRow(data);
        }
    }//GEN-LAST:event_txtcariKeyReleased

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        if(kdbuku.getText().equalsIgnoreCase("")||nonota.getText().equalsIgnoreCase("")||judulbuku.getText().equalsIgnoreCase("")||nmpembeli.getText().equalsIgnoreCase("")
                ||hargajual.getText().equalsIgnoreCase("")||jumlah.getText().equalsIgnoreCase("")||ttotal.getText().equalsIgnoreCase("")||ttunai.getText().equalsIgnoreCase("")
                ||tKembali.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Data Harus Lengkap");
        }else{
         int jml=Integer.parseInt(jumlah.getText());
         int stok=Integer.parseInt(tatok.getText());
         if(jml>stok){
             JOptionPane.showMessageDialog(this, "Stok Buku Tidak Cukup!");
         }else{
             ListDb=pc.CekBuku(nmpembeli.getText(), kdbuku.getText());
             if(ListDb.size()>0){
                 JOptionPane.showMessageDialog(this, "Buku sudah di Beli");
             }else{
                 int harga = Integer.parseInt(hargajual.getText());
                 datapenjualan dp = new datapenjualan();
                 dp.setNonota(nonota.getText());
                 dp.setIDBK(kdbuku.getText());
                 dp.setJudul(judulbuku.getText());
                 dp.setHargaJ(harga);
                 dp.setJumlahB(jml);
                 dp.setTotalB(harga*jml);
                 if(pc.tambahpenjualan(dp) == 0){
                      JOptionPane.showMessageDialog(this, "Data gagal disimpan");
                 }else{
                     int sisaStok = stok-jml;
                     ob.updateStok(sisaStok,kdbuku.getText());
                     buatTableBeli();
                     tampilbeli();
                     int total = Integer.parseInt(ttotal.getText());
                     int totalbyr = total+(harga*jml);
                     ttotal.setText(Integer.toString(totalbyr));
                     JOptionPane.showMessageDialog(this, "Data Telah ditambahkan");
                     kdbuku.setText(null);
                     judulbuku.setText(null);
                     hargajual.setText(null);
                     jumlah.setText(null);
                     ttunai.setText("0");
                     tKembali.setText("0");
                 }
             }
         }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void ttunaiKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ttunaiKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()== 10){
            int total = Integer.parseInt(ttotal.getText());
            int bayar = Integer.parseInt(ttunai.getText());
            if (bayar<total){
                JOptionPane.showMessageDialog(this, "Jumlah Bayar Tidak Cukup");        
            }else{
                int kembali = bayar-total;
                tKembali.setText(Integer.toString(kembali));
            }
        }
    }//GEN-LAST:event_ttunaiKeyPressed

