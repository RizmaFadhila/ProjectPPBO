    private void tampilbuku(){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        ListBuku.clear();
        ListBuku = ob.tampil();
        for(int x=0;x<ListBuku.size();x++){
            Object[] data = new Object[6];
            data[0]= ListBuku.get(x).getId();
            data[1]= ListBuku.get(x).getJDL();
            data[2]= ListBuku.get(x).getGenre();
            data[3]= ListBuku.get(x).getPenulis();
            data[4]= ListBuku.get(x).getHarga();
            data[5]= ListBuku.get(x).getStok();
            model.addRow(data);
        }   
    }
