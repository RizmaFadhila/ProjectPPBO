    private void tampilbeli(){
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        ListDPJ.clear();
        ListDPJ = pc.CariBuku(nonota.getText());
        for(int x=0;x < ListDPJ.size();x++){
            Object[] data = new Object[5];
            data[0]= ListDPJ.get(x).getIDBK();
            data[1]= ListDPJ.get(x).getJudul();
            data[2]= ListDPJ.get(x).getHargaJ();
            data[3]= ListDPJ.get(x).getJumlahB();
            data[4]= ListDPJ.get(x).getTotalB();
            model.addRow(data);
        }
    }
